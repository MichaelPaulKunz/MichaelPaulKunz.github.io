/**
 * input: number, number, number
 * ouput: aray
 * edge: no step provided
 */ 
function range(num1, num2, step) {
  //declare array
  let arr = [];
  let n;
  if (step === undefined){ //set default to 1
    n = 1;
  } else{
    step = n;
  }
  //case 1, num1 smaller
  if (num1 < num2){
    for (let i = num1; i <= num2; i += n){
      arr.push(i);
    }//end loop
  //case 2, num2 smaller  
  } else if (num2 < num1){
    for (let i = num2; i <= num2; i += n){
      arr.push(i);
    }//end loop
  }
  return arr;
}

