////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {

 //input: 2 numbers
 //output: smaller number
 if (num1 < num2){ //first num smaller
   return num1;
 } else { //second num smaller
   return num2;
 }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

/**
 * input: number
 * output: boolean
 * constraints: positive or negative
 * edge cases: none
 **/ 
function isEven(num) {
  if (num === 0){ 
    return true;//zero is even
  } else if (num === 1 || num === -1){
    return false; //1, -1 are odd
  } else {//recurse to 0 or 1
     return num > 0 ? isEven(num - 2) :  isEven(num + 2);
  }//end conditionals
}//end function

