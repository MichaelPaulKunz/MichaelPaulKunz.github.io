// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(strng, char) {
  let counter = 0; //set B's to zero
  //search loop
  for (let i = 0; i < strng.length; i ++){
    if (strng[i] === char){//look for char
      counter++; //add char
    }//end if
  }//end loop
  return counter;

}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

/**
 * input: string
 * output: number
 * constraints: input must be string
 * edge case: none
 **/ 
function countBs(strng) {
  let counter = 0; //set B's to zero
  //search loop
  for (let i = 0; i < strng.length; i ++){
    if (strng[i] === 'B'){//look for B
      counter++; //add B
    }//end if
    
  }//end loop
  return counter;
}

